class SwapValue//swap two value using third variabble
{
	public static void main(String args[])
	{
		int A,B;
		A=10;
		B=20;
			System.out.println("ARTER SWAPING");
			System.out.println("A is "+A);
			System.out.println("B is "+B);
		int temp;	
		temp=B;
		B=A;
		A=temp;
			System.out.println("BEFOR SWAPING");
			System.out.println("A is "+A);
			System.out.println("B is "+B);
	}
}